
/**
 Introducción a primer programa Java
 Cosas a tener en cuenta:
 Java es main sensitive, es decir, distingue entre mayúsuculas y minúsculas.
 System.out.println, se debe escribir, pues, empezando con la "S" mayúsucula,
 por ejemplo. Al igual que "main" debe ser escrito en minúscula.
 
 Public: modificador de acceso.
 TODO PROGRAMA Java debe estar contenido dentro de, al menos, una clase.
 TODO PROGRAMA Java empieza con una llave de apertura y acaba con otra de cierre.
 Se comienzan los programas con una clase.
 
 Nomenclatura de clases:
 No deben tener espacios en blanco.
 No deben tener caracteres especiales.
 Deeben de empezar por una letra mayúscula y siempre por una letra, nunca por
 un número.
 
 El archivo que se crea .java debe llamarse exactamente igual que la clase.
 
 Las sentencias terminan en ";". Las clases no terminan en ";".
 */


public class PrimerEjemplo {
    public static void main(String[]args) {
        System.out.println("¡Hola, alumnos!");
    }
}

